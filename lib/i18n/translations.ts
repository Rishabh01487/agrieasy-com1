export type TranslationKey = keyof typeof translations.en

export function getTranslation(lang: Language, key: TranslationKey): string {
  return translations[lang]?.[key] || translations.en[key] || key
}

// Helper to translate commodity names
export function translateCommodity(lang: Language, name: string): string {
  if (lang === 'en') return name
  const key = name.toLowerCase().replace(/\s+/g, '')
  const map: Record<string, string> = {
    wheat: 'गेहूं', rice: 'चावल', maize: 'मक्का', barley: 'जौ',
    paddy: 'धान', oilseeds: 'तिलहन', cotton: 'कपास', sugarcane: 'गन्ना',
    soybean: 'सोयाबीन', onion: 'प्याज', potato: 'आलू', tomato: 'टमाटर',
    mustard: 'सरसों', gram: 'चना', 'turdal': 'तुर दाल',
    vegetables: 'सब्जियां', fruits: 'फल', spices: 'मसाले', pulses: 'दालें',
  }
  return map[key] || name
}

